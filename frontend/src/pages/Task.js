const Task = () => {
  const { topic, lessonNumber, taskNumber } = useParams();
  const navigate = useNavigate();
  const lessonNum = parseInt(lessonNumber, 10);
  const taskNum = parseInt(taskNumber, 10);
  const [answered, setAnswered] = useState(false);
  const [isCorrect, setIsCorrect] = useState(false);
  const [lives, setLives] = useState(null);
  const [taskData, setTaskData] = useState(null);
  const [taskState, setTaskState] = useState({});
  const [showReference, setShowReference] = useState(false);

  // Загружаем жизни и данные задания
  useEffect(() => {
    const loadData = async () => {
      try {
        const livesRes = await livesAPI.getMyLives();
        setLives(livesRes.data);
        
        // Загружаем данные задания
        let task = null;
        if (topic === 'rent' && lessonNum === 3) {
          task = getRentLesson3Tasks(taskNum);
        } else if (topic === 'rent' && lessonNum === 4) {
          task = getRentLesson4Tasks(taskNum);
        } else if (topic === 'rent' && lessonNum === 5) {
          task = getRentLesson5Tasks(taskNum);
        }
        setTaskData(task);
        
        // Сбрасываем состояние при переходе на новое задание
        setAnswered(false);
        setIsCorrect(false);
        setShowReference(false);
        
        // Инициализируем состояние задания
        if (task) {
          initializeTaskState(task);
        }
      } catch (error) {
        console.error('Ошибка загрузки данных:', error);
      }
    };
    loadData();
  }, [topic, lessonNum, taskNum]);

  const initializeTaskState = (task) => {
    if (task.type === 'multiple_choice') {
      setTaskState({ selected: [] });
    } else if (task.type === 'highlight') {
      // Инициализируем highlights из task.highlights
      const highlights = task.highlights ? task.highlights.map(h => ({ ...h, selected: false, clicked: false })) : [];
      setTaskState({ selectedHighlights: [], highlights, referenceVisible: false });
    } else if (task.type === 'matching') {
      setTaskState({ 
        matches: task.matches ? task.matches.map(m => ({ ...m })) : [],
        showOptions: null 
      });
    } else if (task.type === 'dropdown') {
      setTaskState({ 
        selectedItems: task.items ? task.items.map(item => ({ itemId: item.id, selected: null })) : [],
        showOptions: null 
      });
    }
  };

  const updateTaskState = (updates) => {
    setTaskState(prev => ({ ...prev, ...updates }));
  };

  // Проверяем, можно ли отправить ответ
  const canSubmit = () => {
    if (!taskData) return false;
    
    if (taskData.type === 'dropdown') {
      // Для dropdown заданий проверяем, что все элементы имеют выбранный ответ
      const selectedItems = taskState.selectedItems || [];
      return taskData.items.every(item => 
        selectedItems.some(si => si.itemId === item.id && si.selected !== null && si.selected !== undefined)
      );
    }
    
    // Для остальных типов заданий всегда можно отправить
    return true;
  };

  // Проверяем, есть ли следующее задание
  const hasNextTask = () => {
    if (topic === 'rent' && lessonNum === 3) {
      return taskNum < 14; // Всего 14 заданий в уроке 3
    } else if (topic === 'rent' && lessonNum === 4) {
      return taskNum < 1; // 1 задание в уроке 4
    } else if (topic === 'rent' && lessonNum === 5) {
      return taskNum < 2; // 2 задания в уроке 5
    }
    return false;
  };

  const handleSubmit = async () => {
    if (!taskData) return;
    
    // Проверяем наличие жизней
    if (lives && lives.current_lives === 0) {
      toast.error('У вас закончились жизни! Вы не можете выполнять задания.');
      return;
    }

    // Если уже ответили правильно, не проверяем снова
    if (answered && isCorrect) {
      return;
    }

    // Проверяем ответы в зависимости от типа задания
    let correct = false;
    
    if (taskData.type === 'multiple_choice') {
      correct = checkMultipleChoice(taskData, taskState.selected || []);
    } else if (taskData.type === 'matching') {
      correct = checkMatching(taskData, taskState.matches || []);
    } else if (taskData.type === 'highlight') {
      correct = checkHighlight(taskData, taskState.selectedHighlights || []);
    } else if (taskData.type === 'dropdown') {
      correct = checkDropdown(taskData, taskState.selectedItems || []);
    }

    // Если ответ неправильный, снимаем жизнь только один раз (при первом ответе)
    if (!correct && !answered) {
      try {
        await livesAPI.useLife();
        const livesRes = await livesAPI.getMyLives();
        setLives(livesRes.data);
        
        if (livesRes.data.current_lives === 0) {
          toast.error('У вас закончились жизни! Вы не можете выполнять задания.');
        } else {
          toast.error('Неправильный ответ. Снята одна жизнь.');
        }
      } catch (error) {
        console.error('Ошибка снятия жизни:', error);
      }
    }

    // Для highlight заданий отмечаем выбранные элементы как "clicked"
    if (taskData.type === 'highlight') {
      const highlights = [...(taskState.highlights || [])];
      const selectedHighlights = taskState.selectedHighlights || [];
      
      highlights.forEach(highlight => {
        if (selectedHighlights.includes(highlight.id)) {
          highlight.clicked = true;
          highlight.selected = true;
        }
      });
      
      updateTaskState({ highlights, referenceVisible: true });
    }

    setAnswered(true);
    setIsCorrect(correct);
    setShowReference(true);

    if (correct) {
      toast.success('Правильный ответ!');
      // TODO: Обновить прогресс заданий
    } else if (taskData.type === 'highlight') {
      toast.error('Не все правильные элементы выбраны или выбраны неправильные.');
    }
  };

  const handleReset = () => {
    setAnswered(false);
    setIsCorrect(false);
    // Не скрываем справку при сбросе - она должна оставаться видимой
    // setShowReference(false); - не вызываем, чтобы справка оставалась
    
    if (taskData.type === 'multiple_choice') {
      setTaskState({ selected: [] });
      // Справка остается видимой через showReference (не меняем его)
    } else if (taskData.type === 'matching') {
      setTaskState({ 
        matches: taskData.matches ? taskData.matches.map(m => ({ ...m, selected: null })) : [],
        showOptions: null 
      });
      // Справка остается видимой через showReference (не меняем его)
    } else if (taskData.type === 'dropdown') {
      setTaskState({ 
        selectedItems: taskData.items ? taskData.items.map(item => ({ itemId: item.id, selected: null })) : [],
        showOptions: null 
      });
    } else if (taskData.type === 'highlight') {
      // Сбрасываем состояние highlight заданий, но сохраняем видимость справки
      const resetHighlights = taskData.highlights ? taskData.highlights.map(h => ({ ...h, clicked: false, selected: false })) : [];
      setTaskState({ 
        selectedHighlights: [],
        highlights: resetHighlights,
        referenceVisible: true // Сохраняем видимость справки
      });
    } else if (taskData.type === 'dropdown') {
      // Сбрасываем состояние dropdown заданий
      setTaskState({ 
        selectedItems: taskData.items ? taskData.items.map(item => ({ itemId: item.id, selected: null })) : []
      });
    }
  };

  const checkMultipleChoice = (task, selected) => {
    const correct = task.correct_answers || [];
    if (selected.length !== correct.length) return false;
    return selected.every(ans => correct.includes(ans));
  };

  const checkMatching = (task, matches) => {
    return matches.every(m => m.selected === m.correct);
  };

  const checkHighlight = (task, selectedIds) => {
    const correctIds = task.highlights.filter(h => h.isCorrect).map(h => h.id);
    const incorrectIds = task.highlights.filter(h => !h.isCorrect).map(h => h.id);
    
    // Проверяем, что выбраны все правильные и ни одного неправильного
    const allCorrectSelected = correctIds.every(id => selectedIds.includes(id));
    const noIncorrectSelected = !incorrectIds.some(id => selectedIds.includes(id));
    
    return allCorrectSelected && noIncorrectSelected && selectedIds.length === correctIds.length;
  };

  const checkDropdown = (task, selectedItems) => {
    // Проверяем, что все элементы имеют выбранный ответ
    const allItemsHaveSelection = task.items.every(item => 
      selectedItems.some(si => si.itemId === item.id && si.selected !== null && si.selected !== undefined)
    );
    
    if (!allItemsHaveSelection) return false;
    
    // Проверяем, что все ответы правильные
    return task.items.every(item => {
      const selected = selectedItems.find(si => si.itemId === item.id);
      return selected && selected.selected === item.correct;
    });
  };

  const handleHighlightClick = (highlightId) => {
    if (answered) return;
    
    const selectedHighlights = [...(taskState.selectedHighlights || [])];
    const index = selectedHighlights.indexOf(highlightId);
    
    if (index === -1) {
      // Добавляем выделение
      selectedHighlights.push(highlightId);
    } else {
      // Убираем выделение
      selectedHighlights.splice(index, 1);
    }
    
    updateTaskState({ selectedHighlights });
  };

  if (!taskData) {
    return (
      <div className="task-page">
        <div className="lesson-page-container">
          <div style={{ padding: '100px 20px', textAlign: 'center' }}>
            Загрузка...
          </div>
        </div>
      </div>
    );
  }

  const isHighlightTask = taskData?.type === 'highlight';
  const highlightReferenceVisible = Boolean(taskState?.referenceVisible);
  const shouldShowReference = Boolean(taskData.reference) && (isHighlightTask ? highlightReferenceVisible : showReference);

  return (
    <div className="task-page">
      <div className="lesson-page-container">
        {/* Шапка */}
        <div className="lesson-page-header">
          <div className="lesson-page-header-content">
            <div className="header-logo">ЛОГОТИП</div>
            <nav className="header-nav">
              <Link to="/dashboard" className="header-nav-link">Задания</Link>
              <Link to="/shop" className="header-nav-link">Магазин</Link>
            </nav>
            <div className="header-social-links">
              <Link to="/settings" className="header-social-icon">
                <Settings size={32} color="#000000" />
              </Link>
              <Link to="/profile" className="header-social-icon">
                <User size={32} color="#000000" />
              </Link>
            </div>
          </div>
        </div>

        {/* Кнопка назад */}
        <div className="lesson-page-back">
          <button 
            onClick={() => navigate(`/topic/${topic}/lesson/${lessonNum}`)}
            className="back-button"
          >
            <ArrowLeft size={20} />
            <span>Назад к уроку</span>
          </button>
        </div>

        {/* Основной контент */}
        <div className="task-content">
          <div className="task-frame">
            {renderTaskContent(taskData, taskState, updateTaskState, answered, isCorrect, handleHighlightClick)}
            
            {/* Справка внутри рамки */}
            {shouldShowReference && (
              <div className={`task-reference ${isHighlightTask ? 'task-reference-highlight' : 'task-reference-default'}`} id="task-reference">
                {renderReference(taskData.reference)}
              </div>
            )}
          </div>

          {/* Кнопка "Ответить" / "Попробовать заново" - скрывается после правильного ответа */}
          {!(answered && isCorrect) && (
            <button 
              onClick={answered && !isCorrect ? handleReset : handleSubmit}
              disabled={(lives && lives.current_lives === 0) || (!answered && !canSubmit())}
              className="task-submit-button"
            >
              {answered && !isCorrect ? 'Попробовать заново' : 'Ответить'}
            </button>
          )}

          {/* Навигация между заданиями */}
          {answered && isCorrect && (
            <div className="task-navigation">
              {hasNextTask() ? (
                <button
                  onClick={() => {
                    navigate(`/topic/${topic}/lesson/${lessonNum}/task/${taskNum + 1}`);
                    window.scrollTo({ top: 0, behavior: 'smooth' });
                  }}
                  className="task-next-button"
                >
                  Следующее задание
                </button>
              ) : (
                <div style={{ textAlign: 'center' }}>
                  <div style={{ 
                    fontSize: '24px', 
                    fontWeight: '700', 
                    color: '#F8A720', 
                    marginBottom: '20px' 
                  }}>
                    Поздравляем! Вы выполнили все задания в этом уроке! 🎉
                  </div>
                  <button
                    onClick={() => navigate(`/topic/${topic}`)}
                    className="task-next-button"
                  >
                    Вернуться к темам
                  </button>
                </div>
              )}
            </div>
          )}
        </div>

        {/* Футер */}
        <div className="lesson-page-footer">
          <div className="lesson-page-footer-content">
            <div className="footer-logo">ЛОГОТИП</div>
          </div>
        </div>
      </div>
    </div>
  );
};

// Рендеринг контента задания в зависимости от типа
const renderTaskContent = (task, taskState, updateTaskState, answered, isCorrect, handleHighlightClick) => {
  switch (task.type) {
    case 'multiple_choice':
      return (
        <MultipleChoiceTask 
          task={task} 
          taskState={taskState} 
          updateTaskState={updateTaskState}
          answered={answered}
          isCorrect={isCorrect}
        />
      );
    case 'highlight':
      return (
        <HighlightTask 
          task={task} 
          taskState={taskState} 
          handleHighlightClick={handleHighlightClick}
          answered={answered}
          isCorrect={isCorrect}
        />
      );
    case 'matching':
      return (
        <MatchingTask 
          task={task} 
          taskState={taskState} 
          updateTaskState={updateTaskState}
          answered={answered}
        />
      );
    case 'dropdown':
      return (
        <DropdownTask 
          task={task} 
          taskState={taskState} 
          updateTaskState={updateTaskState}
          answered={answered}
          isCorrect={isCorrect}
        />
      );
    default:
      return <div>Неизвестный тип задания</div>;
  }
};

// Компонент для заданий с выбором ответа
const MultipleChoiceTask = ({ task, taskState, updateTaskState, answered, isCorrect }) => {
  const selected = taskState.selected || [];

  const handleAnswerSelect = (answerId) => {
    // Блокируем изменение ответа после того, как ответили (до нажатия "Попробовать заново")
    if (answered) return;
    
    if (task.multiple) {
      // Множественный выбор
      const newSelected = selected.includes(answerId) 
        ? selected.filter(id => id !== answerId)
        : [...selected, answerId];
      updateTaskState({ selected: newSelected });
    } else {
      // Одиночный выбор
      updateTaskState({ selected: [answerId] });
    }
  };

  return (
    <div className="multiple-choice-task">
      <h2>{task.title}</h2>
      <div className="task-text">{task.text}</div>
      <div className="task-prompt">Выбери правильный вариант:</div>
      <div className="answers-list">
        {task.answers.map((answer) => {
          const isSelected = selected.includes(answer.id);
          let answerClass = 'answer-option';
          
          if (answered) {
            // При неправильном ответе подсвечиваем только выбранный неправильный вариант красным
            if (!isCorrect) {
              if (isSelected && !task.correct_answers.includes(answer.id)) {
                answerClass += ' answer-incorrect';
              }
            } else {
              // При правильном ответе подсвечиваем правильные варианты зеленым
              const isCorrectAnswer = task.correct_answers.includes(answer.id);
              if (isCorrectAnswer) {
                answerClass += ' answer-correct';
              }
            }
          } else if (isSelected) {
            answerClass += ' answer-selected';
          }

          return (
            <div
              key={answer.id}
              className={answerClass}
              onClick={() => handleAnswerSelect(answer.id)}
              style={{ cursor: answered ? 'default' : 'pointer' }}
            >
              {answer.text}
            </div>
          );
        })}
      </div>
    </div>
  );
};

// Компонент для заданий с выделением текста
const HighlightTask = ({ task, taskState, handleHighlightClick, answered, isCorrect }) => {
  const highlights = taskState.highlights || [];
  const selectedHighlights = taskState.selectedHighlights || [];
  const text = task.text || '';

  // Создаем массив частей текста с встроенными выделениями
  const renderTextWithHighlights = () => {
    if (!highlights || highlights.length === 0) {
      return <span>{text}</span>;
    }

    // Сортируем highlights по позиции в тексте
    const sortedHighlights = [...highlights].sort((a, b) => a.startIndex - b.startIndex);
    
    const parts = [];
    let lastIndex = 0;

    sortedHighlights.forEach((highlight, index) => {
      // Находим реальную позицию фразы в тексте, если индексы неправильные
      let actualStartIndex = highlight.startIndex;
      let actualEndIndex = highlight.endIndex;
      
      // Проверяем, что текст по указанным индексам совпадает с искомым
      const textAtIndices = text.substring(highlight.startIndex, highlight.endIndex);
      if (textAtIndices !== highlight.text) {
        // Если не совпадает, ищем фразу в тексте
        const foundIndex = text.indexOf(highlight.text, lastIndex);
        if (foundIndex !== -1) {
          actualStartIndex = foundIndex;
          actualEndIndex = foundIndex + highlight.text.length;
        }
      }
      
      // Добавляем текст до выделения
      if (actualStartIndex > lastIndex) {
        parts.push({
          type: 'text',
          content: text.substring(lastIndex, actualStartIndex),
          key: `text-${index}`
        });
      }

      // Добавляем выделение
      const isSelected = selectedHighlights.includes(highlight.id);
      let highlightClass = 'text-part';
      
      if (answered) {
        // После ответа показываем правильность/неправильность только выбранных элементов
        if (isSelected) {
          if (highlight.isCorrect) {
            highlightClass += ' part-correct';
          } else {
            highlightClass += ' part-incorrect';
          }
        }
      } else if (isSelected) {
        // До ответа показываем выбранные элементы
        highlightClass += ' part-selected';
      }

      parts.push({
        type: 'highlight',
        content: highlight.text,
        highlight: highlight,
        className: highlightClass,
        key: `highlight-${highlight.id}`
      });

      lastIndex = actualEndIndex;
    });

    // Добавляем оставшийся текст
    if (lastIndex < text.length) {
      parts.push({
        type: 'text',
        content: text.substring(lastIndex),
        key: 'text-end'
      });
    }

    return parts.map(part => {
      if (part.type === 'text') {
        return <span key={part.key}>{part.content}</span>;
      } else {
        return (
          <span
            key={part.key}
            className={part.className}
            onClick={() => !answered && handleHighlightClick(part.highlight.id)}
            style={{ cursor: answered ? 'default' : 'pointer' }}
          >
            {part.content}
          </span>
        );
      }
    });
  };

  // Извлекаем номер объявления из заголовка, если он есть
  const titleWithoutAnnouncement = task.title ? task.title.split('\n\n')[0] : task.title;
  const announcementNumber = task.announcementNumber || '';
  const images = task.images || [];

  return (
    <div className="highlight-task">
      <h2>{titleWithoutAnnouncement}</h2>
      {announcementNumber && (
        <div className="announcement-number">{announcementNumber}</div>
      )}
      {images.length > 0 && (
        <div className="highlight-images">
          {images.map((image, index) => (
            <img 
              key={index}
              src={`/${image}`}
              alt={`Объявление ${index + 1}`}
              className="highlight-image"
            />
          ))}
        </div>
      )}
      <div className="highlight-text">
        {renderTextWithHighlights()}
      </div>
    </div>
  );
};

// Компонент для заданий с выпадающим списком
const DropdownTask = ({ task, taskState, updateTaskState, answered, isCorrect }) => {
  const selectedItems = taskState.selectedItems || [];
  const showOptions = taskState.showOptions;

  const handleDropdownClick = (itemId) => {
    if (answered) return;
    updateTaskState({ showOptions: showOptions === itemId ? null : itemId });
  };

  const handleOptionSelect = (itemId, optionId) => {
    const updatedSelectedItems = selectedItems.map(si => 
      si.itemId === itemId ? { ...si, selected: optionId } : si
    );
    
    // Если элемент еще не был в списке, добавляем его
    if (!updatedSelectedItems.some(si => si.itemId === itemId)) {
      updatedSelectedItems.push({ itemId, selected: optionId });
    }
    
    updateTaskState({ 
      selectedItems: updatedSelectedItems,
      showOptions: null 
    });
  };

  const getSelectedOption = (itemId) => {
    const selected = selectedItems.find(si => si.itemId === itemId);
    return selected ? selected.selected : null;
  };

  // Определяем названия столбцов
  const getColumnHeaders = () => {
    if (task.title.includes('Задание 15')) {
      return ['№', 'Ситуация', 'Кто платит?', 'Пояснение и статья закона'];
    } else if (task.title.includes('Задание 16')) {
      return ['№', 'Ситуация', 'Ответ', 'Пояснение'];
    } else if (task.title.includes('Задание 17')) {
      return ['№', 'Ситуация', 'Ответ', 'Что может сделать арендатор'];
    }
    return ['№', 'Ситуация', 'Ответ', 'Пояснение'];
  };

  const headers = getColumnHeaders();

  return (
    <div className="dropdown-task">
      <h2>{task.title}</h2>
      <table className="dropdown-table">
        <thead>
          <tr>
            {headers.map((header, index) => (
              <th key={index}>{header}</th>
            ))}
          </tr>
        </thead>
        <tbody>
          {task.items.map((item, index) => {
            const selected = getSelectedOption(item.id);
            const isCorrectAnswer = selected === item.correct;
            let cellClass = 'dropdown-cell';
            if (answered) {
              cellClass += isCorrectAnswer ? ' correct' : ' incorrect';
            }

            return (
              <tr key={item.id}>
                <td>{index + 1}</td>
                <td>{item.situation}</td>
                <td style={{ position: 'relative', overflow: 'visible' }}>
                  <div 
                    className={cellClass}
                    onClick={() => handleDropdownClick(item.id)}
                  >
                    {answered && (
                      <span className="result-icon">
                        {isCorrectAnswer ? (
                          <svg width="25" height="25" viewBox="0 0 25 25" fill="none" xmlns="http://www.w3.org/2000/svg">
                            <path fillRule="evenodd" clipRule="evenodd" d="M18.7813 8.30158C19.0864 8.60667 19.0864 9.10133 18.7813 9.40643L11.4896 16.6981C11.1845 17.0032 10.6898 17.0032 10.3847 16.6981L6.21808 12.5314C5.91298 12.2263 5.91298 11.7317 6.21808 11.4266C6.52318 11.1215 7.01784 11.1215 7.32293 11.4266L10.9372 15.0408L17.6764 8.30158C17.9815 7.99648 18.4762 7.99648 18.7813 8.30158Z" fill="#91AE0D" stroke="#91AE0D" strokeLinecap="round" strokeLinejoin="round"/>
                          </svg>
                        ) : (
                          <svg width="25" height="25" viewBox="0 0 25 25" fill="none" xmlns="http://www.w3.org/2000/svg">
                            <path d="M8.81692 16.1831L16.1826 8.81738" stroke="#F8202B" strokeWidth="2.5" strokeLinecap="round"/>
                            <path d="M8.81692 8.81692L16.1826 16.1826" stroke="#F8202B" strokeWidth="2.5" strokeLinecap="round"/>
                          </svg>
                        )}
                      </span>
                    )}
                    {selected ? (
                      <span className="selected-option-text">
                        {task.options.find(o => o.id === selected)?.text}
                      </span>
                    ) : (
                      <span className="placeholder">Выберите вариант ответа</span>
                    )}
                  </div>
                  {showOptions === item.id && !answered && (
                    <div className="options-dropdown options-dropdown-side">
                      {task.options.map(option => (
                        <div
                          key={option.id}
                          className="option-item"
                          onClick={() => handleOptionSelect(item.id, option.id)}
                        >
                          {option.text}
                        </div>
                      ))}
                    </div>
                  )}
                </td>
                <td className={answered ? 'explanation-visible' : 'explanation-hidden'}>
                  <div style={{ minHeight: '60px' }}>
                    {item.explanation}
                  </div>
                </td>
              </tr>
            );
          })}
        </tbody>
      </table>
    </div>
  );
};

